# PYTHON OPERATORS
# Arithmetic operators
print('Addition: ', 2 + 3)          # Addition
print('Subtraction: ', 5 - 2)       # Subtraction
print('Multiplication: ', 3 * 4)    # Multiplication
print('Division: ', 10 / 2)        # Division
print('Floor Division: ', 10 // 3)  # Floor Division
print('Modulus: ', 10 % 3)        # Modulus
print('Exponentiation: ', 2 ** 3)   # Exponentiation

# Assignment operators
x = 5 # Assignment
x += 3 # Addition assignment
print('Addition assignment: ', x) # 8
x -= 2 # Subtraction assignment
print('Subtraction assignment: ', x) # 6
x *= 2 # Multiplication assignment
print('Multiplication assignment: ', x) # 12
x /= 4 # Division assignment
print('Division assignment: ', x) # 3.0
x //= 2 # Floor division assignment
print('Floor division assignment: ', x) # 1.0
x %= 2 # Modulus assignment
print('Modulus assignment: ', x) # 1.0
x **= 3 # Exponentiation assignment
print('Exponentiation assignment: ', x) # 1.0

# Comparison operators
print('Equal to: ', 5 == 5)        # Equal to
print('Not equal to: ', 5 != 3)    # Not equal to
print('Greater than: ', 5 > 3)     # Greater than
print('Less than: ', 5 < 10)       # Less than
print('Greater than or equal to: ', 5 >= 5)  # Greater than or equal to
print('Less than or equal to: ', 5 <= 10)    # Less than or equal to

# Logical operators
print('Logical AND: ', True and False)  # Logical AND
print('Logical OR: ', True or False)    # Logical OR
print('Logical NOT: ', not True)        # Logical NOT